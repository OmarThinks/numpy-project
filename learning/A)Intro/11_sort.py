import numpy as np 


array = np.array([5, 9, 4, 4, 7, 6, 3])

newarray = np.sort(array)
print(newarray)
# [3 4 4 5 6 7 9]



array = np.array(["cars","apple","bannana"])
newarray = np.sort(array)
print(newarray)
# ['apple' 'bannana' 'cars']





array = np.array([[9,5,7,3],[1,6,2,7]])
newarray = np.sort(array)
print(newarray)
# [[3 5 7 9]
# [1 2 6 7]]










